import mongoose from "mongoose";

const AuthSchema = new mongoose.Schema(
  {
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

export const Auth = mongoose.models.auth || mongoose.model("Auth", AuthSchema);
